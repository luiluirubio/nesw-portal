// Sorted alphabetically for datalist
export const phLgusSorted = [...phLgus].sort((a, b) => a.name.localeCompare(b.name))

// Look up province by city/municipality name (case-insensitive)
export function getProvinceByCity(cityName: string): string {
  const match = phLgus.find(l => l.name.toLowerCase() === cityName.toLowerCase())
  return match?.province ?? ''
}

// Group by province for optgroup selects
export function groupByProvince(): Record<string, string[]> {
  return phLgus.reduce<Record<string, string[]>>((acc, l) => {
    if (!acc[l.province]) acc[l.province] = []
    acc[l.province].push(l.name)
    return acc
  }, {})
}
